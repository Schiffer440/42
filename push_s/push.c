#include "push_swap.h"

void	pb(t_pile *p)
{
	p->b[++p->s_b] = p->a[p->s_a];
	p->s_a -= 1;
	ft_printf("pb\n");
}

void	pa(t_pile *p)
{
	p->a[++p->s_a] = p->b[p->s_b];
	p->s_b -= 1;
	ft_printf("pa\n");
}